package com.equalize.model.enums;

/**
 * Main playing roles used in the team formation system.
 * Based on the "Roles in Gaming" document.
 */
public enum RoleType {
    STRATEGIST,   // plans tactics, shot-caller
    ATTACKER,     // frontline / damage dealer
    DEFENDER,     // protection / backline
    SUPPORTER,    // flex / utility / healer type
    COORDINATOR   // communication and organisation
}
